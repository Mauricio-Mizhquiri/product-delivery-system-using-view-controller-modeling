package trabajofinal.modelo;

import java.util.ArrayList;

/**
 *
 * @author paulc
 */
public class Cliente {

    private String nombre;
    private String direccion;
    private String telefono;
    private String correo;
    private boolean activo;
    private ArrayList<Ciudad> ciudad;
    public Cliente(ArrayList ciudad,boolean activo,String nombre, String direccion, String telefono, String correo) {
        this.nombre = nombre;
        this.direccion = direccion;
        this.telefono = telefono;
        this.correo = correo;
        this.activo = activo;
        this.ciudad = ciudad;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getDireccion() {
        return direccion;
    }

    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    public String getTelefono() {
        return telefono;
    }

    public void setTelefono(String telefono) {
        this.telefono = telefono;
    }

    public String getCorreo() {
        return correo;
    }

    public void setCorreo(String correo) {
        this.correo = correo;
    }

    public boolean isActivo() {
        return activo;
    }

    public void setActivo(boolean activo) {
        this.activo = activo;
    }

    public ArrayList<Ciudad> getCiudad() {
        return ciudad;
    }

    public void setCiudad(ArrayList<Ciudad> ciudad) {
        this.ciudad = ciudad;
    }

    
}
